package frc.utils;

public class Setpoints {
    public double arm;
    public boolean wrist;
    public double outtake;
    public boolean rollerbar;

    public Setpoints(double arm, boolean wrist, double outtake, boolean rollerbar) {
        this.arm = arm;
        this.wrist = wrist;
        this.outtake = outtake;
        this.rollerbar = rollerbar;
    }
}
